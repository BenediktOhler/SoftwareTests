public class Taschenrechner {

    public int int_Addition(int summand1,int summand2) {
        return summand1 + summand2;
    }

    public float float_Addition(float summand1,float summand2){
        return summand1 + summand2;
    }

    public int int_Subtraktion(int minuend,int subtrahend){
        return minuend - subtrahend;
    }

    public float float_Subtraktion(float minuend,float subtrahend){
        return minuend - subtrahend;
    }

    public int int_Multiplikation(int multiplikand,int multiplikator) {
        return multiplikand * multiplikator;
    }

    public float float_Multiplikation(float multiplikand,float multiplikator) {
        return multiplikand * multiplikator;
    }

    public int int_Division(int dividend,int divisor) {
        return dividend / divisor;
    }

    public float float_Division(float dividend,float divisor) {
        return dividend / divisor;
    }




}
